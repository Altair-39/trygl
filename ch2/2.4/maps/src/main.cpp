// settings
const unsigned int SCR_WIDTH = 800;
const unsigned int SCR_HEIGHT = 1200;

// camera
Camera camera(glm::vec3(0.0f, 0.0f, 3.0f));
float lastX = SCR_WIDTH / 2.0f;
float lastY = SCR_HEIGHT / 2.0f;
bool firstMouse = true;

// timing
float deltaTime = 0.0f;
float lastFrame = 0.0f;

// lighting
glm::vec3 lightPos(1.2f, 1.0f, 2.0f);

// Global variables for shader and textures
Shader *lightingShader = nullptr;
Shader *lightCubeShader = nullptr;
unsigned int texture1, texture2;

unsigned int diffuseMap;
unsigned int specularMap;

int main() {
  // Initialize GLFW and create window
  GLFWwindow *window = initializeGLFW();
  if (!window) {
    return -1;
  }

  // Initialize GLAD
  if (!initializeGLAD()) {
    return -1;
  }

  // configure global opengl state
  glEnable(GL_DEPTH_TEST);

  // Initialize shaders
  lightingShader = new Shader("colors.vs", "colors.fs");
  lightCubeShader = new Shader("light_cube.vs", "light_cube.fs");

  // Setup vertex data and buffers
  unsigned int cubeVAO = setupCubeVAO();
  unsigned int lightVAO = setupLightVAO();

  diffuseMap = loadTexture("container2.png");
  specularMap = loadTexture("container2_specular.png");

  // Main render loop
  renderScene(window, cubeVAO, lightVAO, *lightingShader, *lightCubeShader);

  // Cleanup
  cleanup(cubeVAO, lightVAO, 0, texture1, texture2);
  delete lightingShader;
  delete lightCubeShader;

  glfwTerminate();
  return 0;
}

GLFWwindow *initializeGLFW() {
  glfwInit();
  glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
  glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
  glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);

  GLFWwindow *window =
      glfwCreateWindow(SCR_WIDTH, SCR_HEIGHT, "LearnOpenGL", NULL, NULL);
  if (window == NULL) {
    std::cout << "Failed to create GLFW window" << std::endl;
    glfwTerminate();
    return nullptr;
  }
  glfwMakeContextCurrent(window);
  glfwSetFramebufferSizeCallback(window, framebuffer_size_callback);
  glfwSetCursorPosCallback(window, mouse_callback);
  glfwSetScrollCallback(window, scroll_callback);

  // tell GLFW to capture our mouse
  glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);

  return window;
}

bool initializeGLAD() {
  if (!gladLoadGLLoader((GLADloadproc)glfwGetProcAddress)) {
    std::cout << "Failed to initialize GLAD" << std::endl;
    return false;
  }
  return true;
}

